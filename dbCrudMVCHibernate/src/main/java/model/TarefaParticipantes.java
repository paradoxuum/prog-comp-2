package model;

public class TarefaParticipantes {

	private Integer id_participante;
	private Integer id_tarefa;
	private Integer id_pessoa;
	
	public Integer getId_participante() {
		return id_participante;
	}
	
	public void setId_participante(Integer id_participante) {
		this.id_participante = id_participante;
	}
	
	public Integer getId_tarefa() {
		return id_tarefa;
	}
	
	public void setId_tarefa(Integer id_tarefa) {
		this.id_tarefa = id_tarefa;
	}
	
	public Integer getId_pessoa() {
		return id_pessoa;
	}
	
	public void setId_pessoa(Integer id_pessoa) {
		this.id_pessoa = id_pessoa;
	}
	
	
}
